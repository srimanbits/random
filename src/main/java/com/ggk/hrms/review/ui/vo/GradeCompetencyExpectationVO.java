package com.ggk.hrms.review.ui.vo;


import java.io.Serializable;
import java.sql.Timestamp;
import java.util.Date;



public class GradeCompetencyExpectationVO implements Serializable {
	private static final long serialVersionUID = 1L;

	private int gradeCompetencyExpectationId;

	private Date createdDate;

	
	private boolean isActive;

	private Date lastModifiedDate;


	private GradeVO grade;


	private ProficiencyLevelCompetencyVO proficiencyLevelCompetency;

    public GradeCompetencyExpectationVO() {
    }

	public int getGradeCompetencyExpectationId() {
		return this.gradeCompetencyExpectationId;
	}

	public void setGradeCompetencyExpectationId(int gradeCompetencyExpectationId) {
		this.gradeCompetencyExpectationId = gradeCompetencyExpectationId;
	}

	public Date getCreatedDate() {
		return this.createdDate;
	}

	public void setCreatedDate(Timestamp createdDate) {
		this.createdDate = createdDate;
	}

	public boolean getIsActive() {
		return this.isActive;
	}

	public void setIsActive(boolean isActive) {
		this.isActive = isActive;
	}

	public Date getLastModifiedDate() {
		return this.lastModifiedDate;
	}

	public void setLastModifiedDate(Timestamp lastModifiedDate) {
		this.lastModifiedDate = lastModifiedDate;
	}

	public GradeVO getGrade() {
		return this.grade;
	}

	public void setGrade(GradeVO grade) {
		this.grade = grade;
	}
	
	public ProficiencyLevelCompetencyVO getProficiencyLevelCompetency() {
		return this.proficiencyLevelCompetency;
	}

	public void setProficiencyLevelCompetency(ProficiencyLevelCompetencyVO proficiencyLevelCompetency) {
		this.proficiencyLevelCompetency = proficiencyLevelCompetency;
	}
	
}