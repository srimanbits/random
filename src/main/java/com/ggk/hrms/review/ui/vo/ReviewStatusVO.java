package com.ggk.hrms.review.ui.vo;

import java.io.Serializable;


public class ReviewStatusVO implements Serializable {
	private static final long serialVersionUID = 1L;

	private int reviewStatusId;

	private String reviewStatusDescription;
	
	private String reviewStatusCode;

	public String getReviewStatusCode() {
		return reviewStatusCode;
	}

	public void setReviewStatusCode(String reviewStatusCode) {
		this.reviewStatusCode = reviewStatusCode;
	}

	public ReviewStatusVO() {
	}

	public int getReviewStatusId() {
		return this.reviewStatusId;
	}

	public void setReviewStatusId(int reviewStatusId) {
		this.reviewStatusId = reviewStatusId;
	}

	public String getReviewStatusDescription() {
		return this.reviewStatusDescription;
	}

	public void setReviewStatusDescription(String reviewStatusDescription) {
		this.reviewStatusDescription = reviewStatusDescription;
	}
}