package com.ggk.hrms.review.ui.vo;

import java.io.Serializable;
import java.util.List;
import java.util.Map;

import com.ggk.hrms.review.beans.domain.DefaultObjectiveGrade;

/**
 * The persistent class for the DefaultObjective database table.
 * 
 */
public class DefaultObjectiveVO implements Serializable {
	private static final long serialVersionUID = 1L;

	private int defaultObjectiveId;

	private int managerEmployeeId;

	private String defaultObjectiveName;

	private Map<Integer, String> gradeMap;

	public Map<Integer, String> getGradeMap() {
		return gradeMap;
	}

	public void setGradeMap(Map<Integer, String> gradeMap) {
		this.gradeMap = gradeMap;
	}

	private List<Integer> grades;

	public int getDefaultObjectiveId() {
		return defaultObjectiveId;
	}

	public void setDefaultObjectiveId(int defaultObjectiveId) {
		this.defaultObjectiveId = defaultObjectiveId;
	}

	public String getDefaultObjectiveName() {
		return defaultObjectiveName;
	}

	public void setDefaultObjectiveName(String defaultObjectiveName) {
		this.defaultObjectiveName = defaultObjectiveName;
	}

	private List<DefaultObjectiveGrade> defaultObjectiveGrades;

	public int getManagerEmployeeId() {
		return managerEmployeeId;
	}

	public void setManagerEmployeeId(int managerEmployeeId) {
		this.managerEmployeeId = managerEmployeeId;
	}

	public List<DefaultObjectiveGrade> getDefaultObjectiveGrades() {
		return defaultObjectiveGrades;
	}

	public void setDefaultObjectiveGrades(
			List<DefaultObjectiveGrade> defaultObjectiveGrades) {
		this.defaultObjectiveGrades = defaultObjectiveGrades;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

	public List<Integer> getGrades() {
		return grades;
	}

	public void setGrades(List<Integer> grades) {
		this.grades = grades;
	}

}